#pragma once
#include "EventCommand.h"
#include "RenderingSystem.h"
#include "Sprite.h"

class TransitionState : public EventCommand
{
public:
	TransitionState();
};

////////////////////////////////////////////////////////////////////////////////////
//					NO TRANSITION
////////////////////////////////////////////////////////////////////////////////////
class NullTransition : public TransitionState
{
public:
	NullTransition();
	void update() override;
	void render(Renderer* r) override;
};

////////////////////////////////////////////////////////////////////////////////////
//					FADE OUT
////////////////////////////////////////////////////////////////////////////////////
class FadeOut : public TransitionState
{
public:
	FadeOut();
	void update() override;
	void render(Renderer* r) override;
private:
	Sprite* m_sprite;
	float m_timer;
};

////////////////////////////////////////////////////////////////////////////////////
//					FADE IN
////////////////////////////////////////////////////////////////////////////////////
class FadeIn : public TransitionState
{
public:
	FadeIn();
	~FadeIn();
	void update() override;
	void render(Renderer* r) override;
private:
	Sprite* m_sprite;
	float m_timer;
};