#pragma once
#include "EventCommand.h"
#include "LayerRenderer.h"
#include "Sprite.h"

class TransitionState : public EventCommand
{
public:
	TransitionState();
};

////////////////////////////////////////////////////////////////////////////////////
//					NO TRANSITION
////////////////////////////////////////////////////////////////////////////////////
class NullTransition : public TransitionState
{
public:
	NullTransition();
	void update() override;
	void render() override;
};

////////////////////////////////////////////////////////////////////////////////////
//					FADE OUT
////////////////////////////////////////////////////////////////////////////////////
class FadeOut : public TransitionState
{
public:
	FadeOut(Sprite* s = nullptr);
	void update() override;
	void render() override;
private:
	Sprite* m_sprite;
	float m_timer;
};

////////////////////////////////////////////////////////////////////////////////////
//					FADE IN
////////////////////////////////////////////////////////////////////////////////////
class FadeIn : public TransitionState
{
public:
	FadeIn(Sprite* s = nullptr);
	~FadeIn();
	void update() override;
	void render() override;
private:
	Sprite* m_sprite;
	float m_timer;
};